#pragma once

/*
 * This is the register mapping for the mpu6886 6 axis gryoscope
 * format:
 *     Register
 *     Values
 */

// REGISTER 25 – SAMPLE RATE DIVIDER
#define MPU6886_SMPLRT_DIV 0x19

// REGISTER 26 – CONFIGURATION
#define MPU6886_CONFIG 0x1A

// REGISTER 27 – GYROSCOPE CONFIGURATION
#define MPU6886_GYRO_CONFIG 0x1B
#define MPU6886_GYRO_FS_SEL_250 0x00
#define MPU6886_GYRO_FS_SEL_500 (1 << 3)
#define MPU6886_GYRO_FS_SEL_1000 (1 << 4)
#define MPU6886_GYRO_FS_SEL_2000 MPU6886_GYRO_FS_SEL_500 | MPU6886_GYRO_FS_SEL_1000

// REGISTER 28 – ACCELEROMETER CONFIGURATION
#define MPU6886_ACCEL_CONFIG 0x1C
#define MPU6886_ACCEL_FS_SEL_2G 0x00
#define MPU6886_ACCEL_FS_SEL_4G (1 << 3)
#define MPU6886_ACCEL_FS_SEL_8G (1 << 4)
#define MPU6886_ACCEL_FS_SEL_16G MPU6886_ACCEL_FS_SEL_4G | MPU6886_ACCEL_FS_SEL_8G

// REGISTER 29 – ACCELEROMETER CONFIGURATION 2
#define MPU6886_ACCEL_CONFIG2 0x1D

// REGISTER 35 – FIFO ENABLE
#define MPU6886_FIFO_EN 0x23
#define MPU6886_GYRO_FIFO_EN (1 << 4)
#define MPU6886_ACCEL_FIFO_EN (1 << 3)

// REGISTER 55 – INT/DRDY PIN / BYPASS ENABLE CONFIGURATION
#define MPU6886_INT_PIN_CFG 0x37
#define MPU6886_INT_LEVEL_LOW (1 << 7)
#define MPU6886_INT_OPEN (1 << 6)
#define MPU6886_LATCH_INT_EN (1 << 5)
#define MPU6886_INT_RD_CLR (1 << 4)
#define MPU6886_FSYNC_INT_LEVEL_LOW (1 << 4)
#define MPU6886_FSYNC_INT_MODE_EN (1 << 2)

// REGISTER 56 – INTERRUPT ENABLE
#define MPU6886_INT_ENABLE 0x38

// REGISTERS 59 TO 64 – ACCELEROMETER MEASUREMENTS
#define MPU6886_ACCEL_XOUT_H 0x3B
#define MPU6886_ACCEL_XOUT_L 0x3C
#define MPU6886_ACCEL_YOUT_H 0x3D
#define MPU6886_ACCEL_YOUT_L 0x3E
#define MPU6886_ACCEL_ZOUT_H 0x3F
#define MPU6886_ACCEL_ZOUT_L 0x40

// REGISTERS 65 AND 66 – TEMPERATURE MEASUREMENT
#define MPU6886_TEMP_OUT_H 0x41
#define MPU6886_TEMP_OUT_L 0x42

// REGISTERS 67 TO 72 – GYROSCOPE MEASUREMENTS
#define MPU6886_GYRO_XOUT_H 0x43
#define MPU6886_GYRO_XOUT_L 0x44
#define MPU6886_GYRO_YOUT_H 0x45
#define MPU6886_GYRO_YOUT_L 0x46
#define MPU6886_GYRO_ZOUT_H 0x47
#define MPU6886_GYRO_ZOUT_L 0x48

// REGISTER 105 – ACCELEROMETER INTELLIGENCE CONTROL
#define MPU6886_ACCEL_INTEL 0x69
#define MPU6886_ACCEL_INTEL_EN (1 << 7)

// REGISTER 106 – USER CONTROL
#define MPU6886_USER_CTRL 0x6A
#define MPU6886_RESET_COND (1 << 0)

// REGISTER 107 – POWER MANAGEMENT 1
#define MPU6886_PM_1 0x6B
#define MPU6886_PM_RESET (1 << 7)
#define MPU6886_PM_SLEEP (1 << 6)
#define MPU6886_PM_CYCLE (1 << 5)
#define MPU6886_PM_GYRO_STDBY (1 << 4)
#define MPU6886_PM_TEMP_DISABLE (1 << 3)
#define MPU6886_PM_CLK_SEL (1 << 0)

// REGISTER 108 – POWER MANAGEMENT 2
#define MPU6886_PM_2 0x6C
#define MPU6886_PM_ACCEL_X_OFF (1 << 5)
#define MPU6886_PM_ACCEL_Y_OFF (1 << 4)
#define MPU6886_PM_ACCEL_Z_OFF (1 << 3)
#define MPU6886_PM_GYRO_X_OFF (1 << 2)
#define MPU6886_PM_GYRO_Y_OFF (1 << 1)
#define MPU6886_PM_GYRO_Z_OFF (1 << 0)

// REGISTER 117 – WHO AM I
#define MPU6886_CHIP_ID 0x75
#define MPU6886_ID 0x19

